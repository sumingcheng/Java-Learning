public class Test2 {
    public static void main(String[] args) {
        /*
         * 3.3-1.4 => 1.9 ?
         * 3.3 整数部分 3
         * 3/2 = 1 余数 1
         * 1/2 = 0 余数 1
         * => 11
         *
         * 3.3 小数部分 0.3
         * 0.3 * 2 = 0.6 | 0
         *
         * 0.6 * 2 = 1.2 | 1
         * 0.2 * 2 = 0.4 | 0
         * 0.4 * 2 = 0.8 | 0
         * 0.8 * 2 = 1.6 | 1
         *
         * 0.6 * 2 = 1.2 | 1
         * 0.2 * 2 = 0.4 | 0
         * 0.4 * 2 = 0.8 | 0
         * 0.8 * 2 = 1.6 | 1
         * => 01001100110011001100110
         *
         * 3.3 => 11.01001100110011001100110
         * 规范化写法
         * 符号位：s => 0
         * 有效数字：M => 1.101001100110011001100110
         * 指数位：E => 1
         * v => (-1)^s * M * 2^E
         * v => (-1)^0 * 0.101100110011001100110110 * 2^1
         * */

        /*
         * 1.4 整数部分 1
         * 1/2 = 0 余数 1
         * => 1
         *
         * 1.4 小数部分 0.4
         * 0.4 * 2 = 0.8 | 0
         * 0.8 * 2 = 1.6 | 1
         * 0.6 * 2 = 1.2 | 1
         * 0.2 * 2 = 0.4 | 0
         * 0.4 * 2 = 0.8 | 0
         * 0.8 * 2 = 1.6 | 1
         * => 01100110011001100110011
         * 1.4 => 1.01100110011001100110011
         * 标准化写法
         * 符号位：s => 0
         * 有效数字：M => 1.01100110011001100110011
         * 指数位：E => 0
         * v => (-1)^s * M * 2^E
         * v => (-1)^0 * 1.01100110011001100110011 * 2^0
         * */

        /*
         * 1.转换
         * 指数位相同
         * v => (-1)^0 * 0.10110011001100110011011 * 2^1
         * 2.运算
         * v => (-1)^0 * 1.101001100110011001100110 * 2^1
         * v => (-1)^0 * 0.101100110011001100110110 * 2^1
         * 相减
         * v => (-1)^0 * 0.111100110011001100110110 * 2^1
         * */

        System.out.println(3.3f - 1.4f); // 1.9f

        /*
         * 1.9 整数部分 1
         * 1/2 = 0 余数 1
         * => 1
         * 1.9 小数部分 0.9
         * 0.9 * 2 = 1.8 | 1
         * 0.8 * 2 = 1.6 | 1
         * 0.6 * 2 = 1.2 | 1
         * 0.2 * 2 = 0.4 | 0
         * 0.4 * 2 = 0.8 | 0
         * 0.8 * 2 = 1.6 | 1
         * 0.6 * 2 = 1.2 | 1
         * => 11100110011001100110011
         * 1.9 => 1.11100110011001100110011
         * 标准化写法
         * 符号位：s => 0
         * 有效数字：M => 1.11100110011001100110011
         * 指数位：E => 0
         * v => (-1)^s * M * 2^E
         * v => (-1)^0 * 1.11100110011001100110011 * 2^0 => 1.9
         * */
    }
}
