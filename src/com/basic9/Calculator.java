package com.basic9;

// 修饰符 class 关键字 类名 {}
public class Calculator {
    public int a = 1;
    public int b = 2;

    public int add () {
        return a + b;
    }

    public int minus () {
        return a - b;
    }

    public int mul () {
        return a * b;
    }

    public int div () {
        return a / b;
    }
}
