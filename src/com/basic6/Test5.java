package com.basic6;

public class Test5 {
    public static void main(String[] args) {
        /*
         * 1. for loop
         * 2. 初始化步数 i = 1
         * 3. 条件判断 i <= 10
         * 4. 进入 循环体 System.out.println(i);
         * 5. 步进 i++，注意，步数操作一定要在循环体的最后
         * */
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
